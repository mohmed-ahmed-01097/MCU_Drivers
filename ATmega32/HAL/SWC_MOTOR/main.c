#ifdef SWC_MOTOR

#include "LBTY_int.h"
#include "LBIT_int.h"
#include "LCTY_int.h"

#include "DELAY.h"
#include "INTP.h"

#include "GPIO_int.h"
#include "MOTOR_int.h"
#include "MOTOR_cfg.h"

int main(void){

	MOTOR_vidInit();
	INTP_vidEnable();

	while(1){
		for(u16 i = 10000 ; i-=100 ; ){
			for(u8 j = MOTOR_CHANNELS ; j-- ; )		MOTOR_vidRun(j, i, MOTOR_ClockWith);
			vidMyDelay_ms(50);
		}
		for(u16 i = 10000 ; i-=100 ; ){
			for(u8 j = MOTOR_CHANNELS ; j-- ; )		MOTOR_vidRun(j, i, MOTOR_AntiClockWith);
			vidMyDelay_ms(50);
		}

		for(u8 j = MOTOR_CHANNELS ; j-- ; )		MOTOR_vidRun(j, 0, MOTOR_Stop);
		vidMyDelay_ms(1000);

	}

	return 0;
}
#endif

