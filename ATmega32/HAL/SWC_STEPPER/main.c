#include "LBTY_int.h"
#include "LBIT_int.h"
#include "LCTY_int.h"

#include "DELAY.h"

#include "GPIO_int.h"
#include "GPIO_cfg.h"

#include "STEPPER_int.h"
#include "STEPPER_cfg.h"

int main(void){

	STEPPER_vidInit();
	u16 u16SetpDelay = STEPPER_NUM_DELAY;
	while(1){
		for(u8 i = 2 ; i-- ; ){
			STEPPER_vidCycle(u16SetpDelay, STEPPER_WithClockWise);
		}
		u16SetpDelay += 50;
		for(u8 i = 2 ; i-- ; ){
			STEPPER_vidCycle(u16SetpDelay, STEPPER_OppositClockWise);
		}
		u16SetpDelay += 50;
	}

	return 0;
}
