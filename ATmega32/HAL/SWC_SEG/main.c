#ifdef	SWC_SEG

#include "LBTY_int.h"
#include "LCTY_int.h"

#include "DELAY.h"

#include "GPIO_int.h"
#include "GPIO_cfg.h"

#include "SEG_int.h"
#include "SEG_cfg.h"

int main(void){

	SEG_vidInit();

	u16 u16Value = 0;
	f32 f32Value = 0.0;

	while(1){

		while(u16Value<100){
			SEG_vidSetNum(u16Value++, LBTY_u8MAX);
			for(u16 i = 40 ; --i ; ){
				while(!SEG_u8Update());
				vidMyDelay_ms(SEG_NUM_DELAY);
			}
		}
		u16Value = 0;
		while(u16Value<100){
			for(u16 i = 40 ; --i ; ){
				SEG_vidDisplayNum(u16Value);
				vidMyDelay_ms(u16Value);
			}
			u16Value++;
		}
		u16Value = 0;
		while(u16Value<100){
			for(u16 i = 40 ; --i ; ){
				SEG_vidDisplayNum_Blink(u16Value);
			}
			u16Value++;
		}
		u16Value = 0;

		while(f32Value<10.0){
			for(u16 i = 40 ; --i ; ){
				SEG_vidDisplayFloat(f32Value);
			}
			f32Value += 0.05;
		}
		f32Value = 0.0;
		while(f32Value<10.0){
			for(u16 i = 40 ; --i ; ){
				SEG_vidDisplayFloat_Blink(f32Value);
			}
			f32Value += 0.05;
		}
		f32Value = 0.0;
	}
	return 0;
}
#endif

