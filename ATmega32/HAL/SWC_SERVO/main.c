#ifdef SWC_SERVO

#include "LBTY_int.h"
#include "LBIT_int.h"
#include "LCTY_int.h"

#include "DELAY.h"
#include "INTP.h"

#include "SERVO_int.h"
#include "SERVO_cfg.h"

static u16 u16Angle = SERVO_MIN_ANGLE;

int main(void){

	Servo_Init();
	INTP_vidEnable();

	vidMyDelay_ms(1000);
	Servo_WriteAngle(180);
	vidMyDelay_ms(1000);
	Servo_WriteAngle(0);
	vidMyDelay_ms(1000);
	Servo_WriteAngle(180);
	vidMyDelay_ms(1000);
	Servo_WriteAngle(90);
	vidMyDelay_ms(1000);
	Servo_WriteAngle(180);
	vidMyDelay_ms(1000);

	u8 u8flage = 1;
	while(1){
		Servo_WriteAngle(u8flage ? u16Angle++ : u16Angle--);
		vidMyDelay_ms(50);
		if(u16Angle == SERVO_MAX_ANGLE){
			u8flage = 0;
		}else if(u16Angle == SERVO_MIN_ANGLE){
			u8flage = 1;
		}
	}

	return 0;
}
#endif

