int main(void){
	LMD_vidInit();
	u8 u8char = 1, u8Arr[150], index = 0;
	while(1){
		while(u8char < 127){
			LMD_u8Char(u8char);
			u8Arr[index++] = u8char++;
		}
		u8Arr[index++] = '\0';

		LMD_vidSetString(u8Arr);
		while(!LMD_u8Update());

		LMD_u8String((u8*)"Hello_MAAM! ");

		LMD_vidSetString((u8*)"Hello_MAAM! ");
		while(!LMD_u8Update())
			vidMyDelay_ms(LMD_CHAR_DELAY);

		LMD_vidSetString((u8*)"https://xantorohara.github.io/led-matrix-editor/");
		while(!LMD_u8Update());
	}
	return 0;
}
